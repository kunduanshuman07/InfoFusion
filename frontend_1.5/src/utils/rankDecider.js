export const rankDecider = (rank) => {
    if(rank===5){
        return "#81099c";
    }
    else if(rank===4){
        return "#147333";
    }
    else if(rank===3){
        return "#9c5714";
    }
    else if(rank===2){
        return "#013973";
    }
    else if(rank===1){
        return "#FFD700";
    }
}
